class Person:
    def __init__(my, name, age, gender):
        my.name = name
        my.age = age
        my.gender = gender

    def introduce(my):
        print(f"Hello, my name is {my.name}. I am {my.age} years old and I am {my.gender}.")


person = Person("Asish", 20, "Male")
person.introduce()
